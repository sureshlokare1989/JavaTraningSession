package JavaBasics;

public class A {
	
	public A() {
		System.out.println("parent class constrcutor");
	}
	
	public A(int i) {
		System.out.println("parent class constructor with value of i is " +i);
	}
	
	public A(int i, int j) {
		System.out.println("parent class constructor with value of i is " +i);
		System.out.println("parent class constructor with value of j is " +j);
	}

}
